
public class TypesAndVariables {
	public static void main(String[] args) {
		//create a data 
		int myVariable; // declaring a variable called myVariable that stores the type int/interger
		myVariable = 5; // initializes myVariable to the value of 5
		
		System.out.println(myVariable);
		
		myVariable = 10;
		System.out.println(myVariable);
		
		myVariable = myVariable + 5;
		//myVariable = 10 + 5
		System.out.println(myVariable);
		
		
		int myVariable2 = 100;
		System.out.println(myVariable2 + myVariable);
		//100 + 15
		
		//fractional value
		double myVariable3 = 342.222;
		System.out.println(myVariable3);
		
		
		int intVariable = 5;
		System.out.println(intVariable);
		//double conversion
		double x = 90.22;		
		System.out.println(x);
		x = intVariable; 
		System.out.println(x);
		
	}

}
